package com.mehmett._00_ExeptionGiris;

public class Personel {
	private String name;
	private String surname;
	
	public String getName() {
		return name;
	}
	
	public String getSurname() {
		return surname;
	}
	
	public Personel(String name, String surname) {
		this.name = name;
		this.surname = surname;
	}
	
	
}